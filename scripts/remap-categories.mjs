// Reads all listings, maps each to a Phase 1 top-level category, and writes
// scripts/migrations/0016_recategorize.sql with one UPDATE per row.
//
// Phase 1 taxonomy (9 top-levels):
//   Gaļa & medījumi
//   Saldēta pārtika
//   Zivis & jūras veltes
//   Maize & konditoreja
//   Dzērieni
//   Olas & piena produkti
//   Dārzeņi, augļi & ogas
//   Iepakotā pārtika
//   Uztura bagātinātāji

import { readFileSync, writeFileSync } from "fs";
import { createClient } from "@supabase/supabase-js";

const raw = readFileSync(".env.local", "utf-8").replace(/^﻿/, "");
const env = {};
for (const line of raw.split(/\r?\n/)) {
  const m = line.match(/^([A-Z_][A-Z0-9_]*)=(.*)$/);
  if (!m) continue;
  let v = m[2].trim();
  if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) v = v.slice(1, -1);
  env[m[1]] = v;
}
const supabase = createClient(env.NEXT_PUBLIC_SUPABASE_URL, env.NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY);

const { data: rows, error } = await supabase
  .from("listings")
  .select("id, title, category, description")
  .eq("status", "active");
if (error) throw error;

// Meat keywords — used to split current "Konservi" between meat-conserves and other-conserves
const MEAT_RX = /\b(brie[dž]a?|alņa|aļņa|cūkas|cūkgaļa|liellopa|teļa|jēra|truša|vistas|gaļa|gaļas|gaļ[īi]g|kūp[ie]n|vītinā|desa|sarde|bumbi[ņn]|konser[v]?|past[ēe]te|mežon[īi]g|ruksis)\b/i;

function remap(row) {
  const c = row.category ?? "";
  const t = (row.title ?? "") + " " + (row.description ?? "").slice(0, 200);

  if (c === "Gaļa") return "Gaļa & medījumi";

  if (c === "Konservi") {
    return MEAT_RX.test(t) ? "Gaļa & medījumi" : "Iepakotā pārtika";
  }

  if (c === "Saldēta pārtika") return "Saldēta pārtika";
  if (c === "Jūras veltes") return "Zivis & jūras veltes";
  if (c === "Konditorija" || c === "Konditorejas") return "Maize & konditoreja";
  if (c === "Dzērieni") return "Dzērieni";
  if (c === "Olas") return "Olas & piena produkti";
  if (c === "Dārzeņi") return "Dārzeņi, augļi & ogas";
  if (c === "Eļļas" || c === "Garšaugi" || c === "Mērces") return "Iepakotā pārtika";
  if (c === "Uztura bagātinātāji") return "Uztura bagātinātāji";
  if (c === "Medus") return "Iepakotā pārtika";

  // Fallback — leave as-is, will print as warning
  return c;
}

const stats = {};
const lines = [];
let unchanged = 0;
const warnings = [];

lines.push("-- AUTO-GENERATED by scripts/remap-categories.mjs");
lines.push("-- Phase 1 taxonomy: 11 categories collapsed to 9 top-levels.");
lines.push("");
lines.push("BEGIN;");
lines.push("");

for (const r of rows) {
  const next = remap(r);
  stats[next] = (stats[next] ?? 0) + 1;
  if (next === r.category) {
    unchanged++;
    if (
      next !== "Saldēta pārtika" && next !== "Dzērieni" && next !== "Uztura bagātinātāji"
    ) {
      // Categories that are RENAMED should not stay unchanged.
      warnings.push(`UNEXPECTED unchanged: ${r.title} | ${r.category} -> ${next}`);
    }
    continue;
  }
  lines.push(`UPDATE listings SET category = '${next.replace(/'/g, "''")}' WHERE id = '${r.id}';`);
}

lines.push("");
lines.push("COMMIT;");

writeFileSync("scripts/migrations/0016_recategorize.sql", lines.join("\n"));

console.log("New category distribution:");
for (const [c, n] of Object.entries(stats).sort((a, b) => b[1] - a[1])) {
  console.log(`  ${c.padEnd(30)} ${n}`);
}
console.log(`\nUnchanged (already correct): ${unchanged}`);
console.log(`UPDATEs in migration: ${rows.length - unchanged}`);
if (warnings.length) {
  console.log("\nWarnings:");
  for (const w of warnings) console.log("  " + w);
}
console.log("\nWritten: scripts/migrations/0016_recategorize.sql");
