/**
 * Bulk-fix listings.locker_id for every seller whose physical drop-off
 * pakomāts differs from their legal address. Re-run anytime — the SQL is
 * idempotent.
 *
 * For sellers who use courier pickup (Buržujs collects from Berga bazārs),
 * we set listings.locker_id to the locker that shares their pickup zone so
 * effectiveCourierZone() prices correctly, AND we write the real pickup
 * address to sellers.courier_pickup_address.
 */
import { readFileSync, writeFileSync } from "fs";
import { createClient } from "@supabase/supabase-js";

const raw = readFileSync(".env.local", "utf-8").replace(/^﻿/, "");
const env = {};
for (const line of raw.split(/\r?\n/)) {
  const m = line.match(/^([A-Z_][A-Z0-9_]*)=(.*)$/);
  if (!m) continue;
  let v = m[2].trim();
  if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) v = v.slice(1, -1);
  env[m[1]] = v;
}
const supabase = createClient(env.NEXT_PUBLIC_SUPABASE_URL, env.NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY);

// Substring match on either name or farm_name. First match wins.
const PLAN = [
  { match: "Bujums",       locker: "agenskalna" },
  { match: "Oranžās",      locker: "agenskalna" },
  { match: "WILD",         locker: "agenskalna" },
  { match: "Ekoloģisks",   locker: "brivibas"   },
  { match: "Cake Break",   locker: "brivibas"   },
  { match: "Dabas",        locker: "brivibas"   }, // NOT in DB — we still emit a no-op comment
  // Buržujs: courier pickup at Berga bazārs (Dzirnavu iela 67, Rīga) — Z0,
  // so locker_id = 'brivibas' for pricing purposes; pickup address goes in
  // sellers.courier_pickup_address.
  { match: "BURŽUJS",      locker: "brivibas",
    courierPickup: "Dzirnavu iela 67, Rīga, LV-1010 (Berga bazārs, Buržujs restorāns)" },
];

const { data: allSellers } = await supabase
  .from("sellers")
  .select("id, user_id, name, farm_name, home_locker_ids, courier_pickup_address, location");

const lines = [];
lines.push("-- AUTO-GENERATED by scripts/sync-all-dropoffs.mjs");
lines.push("-- Forces every seller's listings onto their actual drop-off pakomāts so");
lines.push("-- courier pricing (MAX of seller and buyer zone) works correctly.");
lines.push("");

let totalUpdated = 0;
let migrationsCount = 0;

for (const entry of PLAN) {
  const matched = allSellers.find((s) =>
    (s.name ?? "").toLowerCase().includes(entry.match.toLowerCase()) ||
    (s.farm_name ?? "").toLowerCase().includes(entry.match.toLowerCase())
  );
  if (!matched) {
    console.log(`✗ NOT IN DB: "${entry.match}" — skipping`);
    lines.push(`-- "${entry.match}" not found in sellers table — skipped.`);
    lines.push("");
    continue;
  }

  const display = matched.farm_name ?? matched.name;

  // Count listings that actually need updating
  const { data: listings } = await supabase
    .from("listings")
    .select("id, locker_id")
    .eq("seller_id", matched.id);
  const wrong = (listings ?? []).filter((l) => l.locker_id !== entry.locker);
  console.log(`${display.padEnd(28)} → ${entry.locker.padEnd(11)} | ${listings?.length ?? 0} listings, ${wrong.length} need update`);

  lines.push(`-- ${display} (${matched.id}) → ${entry.locker}`);
  lines.push(`UPDATE listings SET locker_id = '${entry.locker}'`);
  lines.push(`  WHERE seller_id = '${matched.id}' AND locker_id IS DISTINCT FROM '${entry.locker}';`);
  // Always reset home_locker_ids to a single source-of-truth
  lines.push(`UPDATE sellers SET home_locker_ids = ARRAY['${entry.locker}']`);
  lines.push(`  WHERE id = '${matched.id}';`);
  if (entry.courierPickup) {
    const escaped = entry.courierPickup.replace(/'/g, "''");
    lines.push(`UPDATE sellers SET courier_pickup_address = '${escaped}'`);
    lines.push(`  WHERE id = '${matched.id}';`);
  }
  lines.push("");
  totalUpdated += wrong.length;
  migrationsCount++;
}

const filename = "scripts/migrations/0020_dropoff_all_sellers.sql";
writeFileSync(filename, lines.join("\n"));
console.log("");
console.log(`Total listings to update: ${totalUpdated} (across ${migrationsCount} sellers)`);
console.log(`Written: ${filename}`);
