import { readFileSync, writeFileSync } from "fs";
import { createClient } from "@supabase/supabase-js";

const raw = readFileSync(".env.local", "utf-8").replace(/^﻿/, "");
const env = {};
for (const line of raw.split(/\r?\n/)) {
  const m = line.match(/^([A-Z_][A-Z0-9_]*)=(.*)$/);
  if (!m) continue;
  let v = m[2].trim();
  if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) v = v.slice(1, -1);
  env[m[1]] = v;
}
const supabase = createClient(env.NEXT_PUBLIC_SUPABASE_URL, env.NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY);

const { data: rows } = await supabase
  .from("listings")
  .select("id, title, price, status, seller_id")
  .or("price.eq.0,price.is.null");

console.log(`Listings with price = 0 or null: ${rows.length}\n`);

const toDeactivate = rows.filter((r) => r.status === "active");
console.log(`Currently active (will be paused): ${toDeactivate.length}\n`);

for (const r of rows) {
  console.log(`  ${r.status.padEnd(8)} | "${r.title}" | price=${r.price} | seller=${r.seller_id?.slice(0, 8)}`);
}

// Write migration
const lines = [];
lines.push("-- AUTO-GENERATED by scripts/deactivate-zero-price.mjs");
lines.push("-- Pauses listings with price = 0 (or NULL). The seller dashboard");
lines.push("-- shows a 'Cena nav norādīta' banner per affected listing so the");
lines.push("-- seller can fix the price and reactivate.");
lines.push("");
lines.push("UPDATE listings");
lines.push("SET status = 'paused'");
lines.push("WHERE (price = 0 OR price IS NULL) AND status = 'active';");
writeFileSync("scripts/migrations/0017_pause_zero_price.sql", lines.join("\n"));
console.log("\nWritten: scripts/migrations/0017_pause_zero_price.sql");
