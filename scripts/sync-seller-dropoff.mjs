/**
 * Audit + sync a single seller's listing.locker_id to their actual drop-off
 * pakomāts. Some sellers are legally far from Rīga but bring stock to a Rīga
 * locker themselves (WILD'N'FREE, Buržujs, etc.) — the listing's locker_id
 * must reflect the *drop-off*, not the legal address, because that's what
 * effectiveCourierZone() uses to price courier delivery.
 *
 * Usage:
 *   node scripts/sync-seller-dropoff.mjs "WILD'N'FREE" agenskalna
 *
 * The script only AUDITS by default. Add --apply to write a migration SQL.
 */
import { readFileSync, writeFileSync } from "fs";
import { createClient } from "@supabase/supabase-js";

const [, , farmNameArg, lockerIdArg, ...flags] = process.argv;
const apply = flags.includes("--apply");

if (!farmNameArg || !lockerIdArg) {
  console.error("Usage: node scripts/sync-seller-dropoff.mjs \"<farm_name>\" <locker_id> [--apply]");
  process.exit(1);
}

const KNOWN_LOCKERS = ["brivibas", "agenskalna", "salaspils", "ikskile", "tukums", "dundaga"];
if (!KNOWN_LOCKERS.includes(lockerIdArg)) {
  console.error(`Unknown locker_id "${lockerIdArg}". Allowed: ${KNOWN_LOCKERS.join(", ")}`);
  process.exit(1);
}

const raw = readFileSync(".env.local", "utf-8").replace(/^﻿/, "");
const env = {};
for (const line of raw.split(/\r?\n/)) {
  const m = line.match(/^([A-Z_][A-Z0-9_]*)=(.*)$/);
  if (!m) continue;
  let v = m[2].trim();
  if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) v = v.slice(1, -1);
  env[m[1]] = v;
}
const supabase = createClient(env.NEXT_PUBLIC_SUPABASE_URL, env.NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY);

// 1. Locate the seller
const { data: sellerMatches } = await supabase
  .from("sellers")
  .select("id, user_id, farm_name, name, home_locker_ids, courier_pickup_address, location")
  .or(`farm_name.ilike.%${farmNameArg}%,name.ilike.%${farmNameArg}%`);

if (!sellerMatches || sellerMatches.length === 0) {
  console.error(`No seller found matching "${farmNameArg}"`);
  process.exit(1);
}
if (sellerMatches.length > 1) {
  console.log("Multiple matches — pick one and rerun with the exact farm_name:");
  for (const s of sellerMatches) console.log(`  - ${s.farm_name} (id ${s.id})`);
  process.exit(1);
}

const seller = sellerMatches[0];
console.log("Seller:");
console.log(`  farm_name        : ${seller.farm_name}`);
console.log(`  location (legal) : ${seller.location ?? "(none)"}`);
console.log(`  home_locker_ids  : ${JSON.stringify(seller.home_locker_ids)}`);
console.log(`  courier pickup   : ${seller.courier_pickup_address ?? "(none)"}`);
console.log("");

// 2. Pull their listings — listings.seller_id is the sellers.id (not auth user_id)
const { data: listings } = await supabase
  .from("listings")
  .select("id, title, locker_id, status")
  .eq("seller_id", seller.id);

if (!listings || listings.length === 0) {
  console.log(`No listings found for seller_id=${seller.id}`);
  process.exit(0);
}

const wrong = listings.filter((l) => l.locker_id !== lockerIdArg);
const correct = listings.length - wrong.length;

console.log(`Listings: ${listings.length} total · ${correct} already on "${lockerIdArg}" · ${wrong.length} need updating`);
console.log("");
for (const l of listings) {
  const flag = l.locker_id === lockerIdArg ? "✓" : "✗";
  console.log(`  ${flag} ${l.status.padEnd(15)} ${l.locker_id?.padEnd(11) ?? "(null)     "} "${l.title}"`);
}
console.log("");

if (wrong.length === 0) {
  console.log("Nothing to do — all listings already point at the correct drop-off pakomāts.");
  process.exit(0);
}

// 3. Update home_locker_ids on the seller too, so future listings inherit it
const sellerNeedsUpdate = !(seller.home_locker_ids ?? []).includes(lockerIdArg);
if (sellerNeedsUpdate) {
  console.log(`(Seller's home_locker_ids will also be set to ["${lockerIdArg}"] so new listings default to it.)`);
  console.log("");
}

// 4. Write migration SQL
const displayName = seller.farm_name ?? seller.name ?? "seller";
const lines = [];
lines.push("-- AUTO-GENERATED by scripts/sync-seller-dropoff.mjs");
lines.push(`-- Forces all listings for "${displayName}" onto drop-off pakomāts "${lockerIdArg}".`);
lines.push("-- This seller is legally located elsewhere but physically drops stock here,");
lines.push("-- so courier zone (MAX of seller and buyer zone) must use the drop-off pakomāts.");
lines.push("");
if (sellerNeedsUpdate) {
  lines.push(`UPDATE sellers`);
  lines.push(`SET home_locker_ids = ARRAY['${lockerIdArg}']`);
  lines.push(`WHERE id = '${seller.id}';`);
  lines.push("");
}
lines.push(`UPDATE listings`);
lines.push(`SET locker_id = '${lockerIdArg}'`);
lines.push(`WHERE seller_id = '${seller.id}'`);
lines.push(`  AND (locker_id IS DISTINCT FROM '${lockerIdArg}');`);

const migrationNumber = "0019";
const safeName = displayName.toLowerCase().replace(/[^a-z0-9]+/g, "_").replace(/^_|_$/g, "");
const filename = `scripts/migrations/${migrationNumber}_dropoff_${safeName}.sql`;
writeFileSync(filename, lines.join("\n") + "\n");
console.log(`Written: ${filename}`);
console.log("");
console.log("Apply via Supabase SQL Editor (the file is also copied to clipboard manually).");
